int main() {
    int H, W;
    cin>>H>>W;

    vector<string> grid(H);
    for (int i = 0; i < H; i++) {
        cin>>grid[i];
    }
    int min_row = H, max_row = -1, min_col = W, max_col = -1;

    for (int i = 0; i < H; i++) {
        for (int j = 0; j < W; j++) {
            if (grid[i][j] == '#') {
                min_row = min(min_row, i);
                max_row = max(max_row, i);
                min_col = min(min_col, j);
                max_col = max(max_col, j);
            }
        }
    }
    bool is_possible = true;
    for (int i = 0; i < H; i++) {
        for (int j = 0; j < W; j++) {
            if (i >= min_row && i <= max_row && j >= min_col && j <= max_col) {
                if (grid[i][j] == '.') {
                    is_possible = false;
                }
            } 
            else {
                if (grid[i][j] == '#') {
                    is_possible = false;
                }
            }
        }
    }
    cout << (is_possible ? "Yes" : "No") << endl;
    return 0;
}



