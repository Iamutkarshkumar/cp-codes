int main(){
    file_i_o();
    int t;
    cin>>t;
    while(t--){
        int n,m;cin>>n>>m;
        vector<pair<int,int>> pos; 
        int gmax = 0, smax = 0;
        for(int i=0;i<n;i++){
            for(int j = 0;j<m;j++){
                int x;cin>>x;
                if(x > gmax){
                    smax = gmax;
                    gmax = x;
                    pos.clear();
                    pos.push_back({i, j});
                }
                else if(x == gmax) pos.push_back({i, j});
                else if(x > smax) smax = x;
                
            }
        }
        int r0 = pos[0].first, c0 = pos[0].second;
        bool flag = true;
        int col_need = -1;
        for(auto p : pos){
            if(p.first!=r0){
                if(col_need<0) col_need = p.second;
                else if(col_need != p.second){
                    flag = false;
                    break;
                }
            }
        }
        if(not flag){
            flag = true;
            int row_need = -1;
            for(auto p : pos){
                if(p.second!=c0){
                    if(row_need<0) row_need = p.first;
                    else if(row_need != p.first){
                        flag = false;
                        break;
                    }
                }
            }
        }
        cout<<(flag ? max(gmax-1, smax) : gmax)<<endl;
    }
}

